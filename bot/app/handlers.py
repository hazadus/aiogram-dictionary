        "‚Ä¢ –ü–µ—Ä–µ–≤–æ–¥–∏—Ç—å —Å–ª–æ–≤–∞ –∏ —Ñ—Ä–∞–∑—ã –Ω–∞ —Ä—É—Å—Å–∫–∏–π —è–∑—ã–∫\n"
